package entities;

public class Educator {
	
	private String educatorName;
	private String educatorInfo;
	private String educatorImage;
	
	
	public String getEducatorName() {
		return educatorName;
	}
	public void setEducatorName(String educatorName) {
		this.educatorName = educatorName;
	}
	public String getEducatorInfo() {
		return educatorInfo;
	}
	public void setEducatorInfo(String educatorInfo) {
		this.educatorInfo = educatorInfo;
	}
	public String getEducatorImage() {
		return educatorImage;
	}
	public void setEducatorImage(String educatorImage) {
		this.educatorImage = educatorImage;
	}
	
	

}
